// ReSharper disable once CheckNamespace
namespace Mantra;

/// <summary>
/// The direction an animation slides in (the slide out direction is reversed)
/// </summary>
internal enum AnimationSlideInDirection
{
    /// <summary>
    /// Slide in from left
    /// </summary>
    Left = 0,

    /// <summary>
    /// Slide in from right
    /// </summary>
    Right = 1,

    /// <summary>
    /// Slide in from top
    /// </summary>
    Top = 2,

    /// <summary>
    /// Slide in from bottom
    /// </summary>
    Bottom = 4
}